    public void Location(int x, int y) {
        try {
            golf_ball_lbl.setVisible(true);
            Dimension dimensionsize = golf_ball_lbl.getPreferredSize();
            golf_ball_lbl.setBounds((x - dimensionsize.width / 2), (y - dimensionsize.height), dimensionsize.width, dimensionsize.height);
            ground_lbl.add(golf_ball_lbl);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
