    private void ground_lblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ground_lblMouseClicked
        try {
            int x = evt.getX();
            int y = evt.getY();
            Location(x, y);
            originator.setPosition(new Position(x, y));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_ground_lblMouseClicked

    private void undo_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undo_buttonActionPerformed
        try {
            originator.undo();
            if (originator.getPosition() != null) {
                Location(originator.getPosition().getX(), originator.getPosition().getY());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_undo_buttonActionPerformed

